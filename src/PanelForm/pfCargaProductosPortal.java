    private void btCvsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCvsActionPerformed
    
        if(Grilla.getRowCount()==0){
            fmMain.Mensaje("No hay Productos para exportar");
            return;
        }
        
        File archivo = null;
        File folder = new File("C:/temp");
        String nombre = "lista"+mes+agno;
        String sSistemaOperativo = System.getProperty("os.name").toString().toLowerCase();       //Determina el Sistema Operativo del Equipo
        
        if (sSistemaOperativo.contains("win")){
             
            if (!folder.exists()){
                folder.mkdir();
            }
            
            archivo = new File("C:/temp/lista"+mes+agno+".csv");
                
        }else{
            
            JFileChooser elegir = new JFileChooser();
            FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos CSV","csv");
            elegir.setSelectedFile(new File("/user","lista"+mes+agno+".csv"));
            elegir.setFileFilter(filtro);
            elegir.setDialogTitle("Guardar archivo");
            elegir.setAcceptAllFileFilterUsed(false);
            
            if (elegir.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
                String file2 = elegir.getSelectedFile().toString().concat(".csv");
                
                 archivo = new File(file2);  
                
                try {
                     Exportercsv exp = new Exportercsv(archivo, Grilla, nombre); 
                    if (exp.export()) {
                        JOptionPane.showMessageDialog(null, "Los datos fueron exportados en el directorio seleccionado", "Mensaje de Informacion", JOptionPane.INFORMATION_MESSAGE);
                        abrir(file2);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Hubo un error " + e.getMessage(), " Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
  
        FileWriter flarchivo = null;
        
        try {
          
             Exportercsv exp = new Exportercsv(archivo, Grilla, nombre); 
 
             if(exp.export()){
             
                String file = archivo.toString();
                           
                abrir(file);
             }
                    
        } catch (IOException ex ) {
            
            Logger.getLogger(pfCargaProductosPortal.class.getName()).log(Level.SEVERE, null, ex);
        
        
        } catch (Exception ex) {
            
            Logger.getLogger(pfCargaProductosPortal.class.getName()).log(Level.SEVERE, null, ex);
        
        }finally{
        
            if (flarchivo != null) {
		
                try {              
	    	    
                    flarchivo.close();   //cierra el flujo principal
                   
                }catch (IOException e){
		
                    e.printStackTrace();
		
                }
                
	    }
            
        }
       
    }//GEN-LAST:event_btCvsActionPerformed

