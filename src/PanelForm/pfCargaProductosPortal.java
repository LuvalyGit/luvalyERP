    public void abrir(String file ){
        
       try {
           String url = file;
           ProcessBuilder p = new ProcessBuilder();
           p.command("cmd.exe", "/c",url);
           p.start();
       } catch (IOException ex) {
           Logger.getLogger(pfCargaProductosPortal.class.getName()).log(Level.SEVERE, null, ex);
       }
        
    }
    
    
    private void carga_producto(){
        
        String codbarfinal = txSku.getText().replace("'", "-");
        String gpublicado = "0";
        txSku.setText(codbarfinal.trim());
        
            if(!txSku.getText().isEmpty() ){
                
                if(CheckIfExist(txSku.getText()) || CheckIDCH(txSku.getText()) || CheckCOD(txSku.getText())){
          
                    try {
                        ResultSet Rs;
                        ExeSql  Sql = new ExeSql();
                        
                        boolean cotiz = false;
                        boolean Insumo = false;
                                                
                        String sql = "SELECT p.sku,id2.ids,p.nombre,i.stock AS stock,p.notransado,p.desxprecio, p.sinstock,p.publicado,psl.sublinea,p.pventa \n" +
                                     "FROM producto p\n" +
                                     "LEFT JOIN inventario i ON p.sku=i.sku \n" +
                                     "LEFT JOIN par_sublinea psl ON p.sublinea=psl.codigo \n" +
                                     "LEFT JOIN (SELECT sku, STRING_AGG (idch, ' - ' ORDER BY idch) AS ids \n" +    //Permite agrupar campos en uno solo
                                     "FROM codchile GROUP BY sku) id2 ON (p.sku = id2.sku)" ;
                        
                        if(Insumo){
                         
                          sql = sql + "where insumo=true\n";
                        
                        }else{
                        
                            sql = sql + "where insumo=false\n";
                        
                        }
                        
                        if ( cotiz ){
                        
                        }else{
                            sql = sql  + " and p.es_cotiza <> 1 \n";
                        }    
                        
                        sql = sql + " and p.sku = '"+txSku.getText()+"'" ;       
                        
                        
                        Rs = Sql.Select(sql);
                        
                        if(Sql.GetRowCount()>0){
                            
                            Rs.next();
                            
                            String sku = Rs.getString("sku");
                            String nom = Rs.getString("nombre");
                            String cat = Rs.getString("sublinea");
                            String id = Rs.getString("ids");
                            String publ = Rs.getString("publicado");
                            double pnormal = (Rs.getDouble("pventa") * 1.19) * 1.05;
                            String precnorm = String.valueOf(Math.round(pnormal));
                            
                            if (publ.equals("t")){
                            
                                publ = "1";
                                
                            }else{
                            
                                publ = "0";
                            }
                            
                            CargaProducto(sku,nom,publ,cat,id,precnorm);
            
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(pfCargaProductosPortal.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                }else {
                
                    JOptionPane.showMessageDialog(null, "No existe producto.");
                    txSku.setText("");
                    
                }
                
            }else{
                
                jdBuscarProductosPortal BPP = new jdBuscarProductosPortal(null, true);
                BPP.setLocationRelativeTo(null);
                BPP.setTitle("Buscar Producto");
                BPP.setVisible(true);
                BPP.SetCotiza(false);
                

                
                if(!"".equals(BPP.GetSku())){
                    
                    if (BPP.GetPublicado().equals("t")){
                            
                        gpublicado = "1";
                                
                    }else{
                            
                        gpublicado = "0";
                    }
                    
                    
                    CargaProducto(BPP.GetSku(),BPP.GetNombre(),gpublicado,BPP.GetCat(),BPP.GetID(),BPP.GetPrecioNorm());
                }
            }
       
    }
    
