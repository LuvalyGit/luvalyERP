    private boolean CargaCliente(String Rut){
    ExeSql Sql = new ExeSql();
    ResultSet Rs;
    String Query = "";
    try {
        
        
    Query =     "select \n" +
" c.rut,c.dv,c.nombre nombre_cliente, p.nombre  || ' - ' ||  p.apellidopaterno  nombre_vendedor,  p.vendedor \n" +
" from cliente c\n" +
" inner join cli_cartera_venta cv\n" +
" on cv.rut_cliente = c.rut\n" +
" left join personal p\n" +
" on p.vendedor = cv.cod_vendedor" +
                    " where c.rut= " + Rut;

    
        Rs = Sql.Select(Query);
        DefaultListModel dfLista = new DefaultListModel();
        lstVendedores.removeAll();
        
        
                while (Rs.next()){
                    txRut.setText(Rs.getString("rut"));
                    txDv.setText(Rs.getString("dv"));
                    txNombre.setText(Rs.getString("nombre_cliente").trim());
                    dfLista.addElement(Rs.getString("nombre_vendedor"));
                }
                
                if (!txNombre.getText().trim().equals("")){
                      lstVendedores.setModel(dfLista);
                      return true;
                }
                else{
                    txDv.setText("");
                    txNombre.setText("");    
                    return false;
                }    
        }
   catch (Exception e) {
    }
        return false;
    }    
    
    
    private void btIrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIrActionPerformed
        boolean Carga;
        if(!txRut.getText().isEmpty()){
            Carga=CargaCliente(txRut.getText());
        }
        else{
            jdBuscarCliCartera BPC = new jdBuscarCliCartera(null, true);
            BPC.setLocationRelativeTo(null);
            BPC.setTitle("Buscar Cliente");
            BPC.SetTipo(1);
            BPC.setVisible(true);
            Carga=CargaCliente(BPC.GetRut());
        }
        
        if (!Carga ){
            fmMain.Mensaje("No Tiene Vendedor de Cartera");
        }
            

    }//GEN-LAST:event_btIrActionPerformed

