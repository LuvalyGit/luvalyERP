    private void AbrirFolio(String Numero){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel TableModel = (DefaultTableModel) Grilla.getModel();
        
        while(TableModel.getRowCount()>0)
           TableModel.removeRow(0);
        
        try {
            //Carga Encabezado
            Rs = Sql.Select("select usuario,cast(fecha as date) as fecha,motivo,comentario from ajusteenc_autoriza where folio=" + Numero);
            if(Sql.GetRowCount()==0){
                fmMain.Mensaje("Folio no encontrado");
                return;
            }
            Rs.next();
            txUsuario.setText(Rs.getString("usuario").trim());
            txFecha.setText(Rs.getString("fecha").trim());
            txComentarios.setText(Rs.getString("comentario"));
            for(int i=0 ; i < cbMotivo.getItemCount();i++){
                cbMotivo.setSelectedIndex(i);
                if(cbId_Accion(cbMotivo)==Rs.getInt("motivo") ){
                    SelMotivo=i;
                    break; 
                }
                    
            }
            lbFolio.setText(Numero);
            
            //Carga Productos
            Rs = Sql.Select("select d.sku,p.nombre,u.unidad,\n" +
                            "case d.tipo when 0 then 'Sumar' when 1 then 'Restar' else 'Dejar en' end as ElTipo, \n"+
                            "d.cantidad,d.tipo, \n"+
                            "case when cm.nombre is null then '' else cm.nombre end as desc_ubica, d.ubicacion cod_ubica \n" +
                            "from ajustedet_autoriza d\n" +
                            "left join producto p on d.sku=p.sku \n" +
                            "left join par_unidad u on p.unidad=u.codigo \n" +
                            "left join mt_codmetro cm on d.ubicacion=cm.codmetro \n" +
                            "where d.folio=" + Numero);
            
            while(Rs.next()){
                
                TableModel.addRow(new Object[]{
                        Rs.getString("Sku").trim(), 
                        Rs.getString("Nombre").trim(), 
                        Rs.getString("unidad").trim(),
                        Rs.getString("ElTipo").trim(),
                        Rs.getDouble("cantidad"),
                        Rs.getInt("tipo"),
                        Rs.getString("desc_ubica").trim(),
                        Rs.getString("cod_ubica").trim()
                });
            }
            
            SetTipo(2);
        } catch (Exception e) {
            System.out.println(e);
            
            Logger.getLogger(pfAjusteStock.class.getName()).log(Level.SEVERE, null, e);
        }finally{
            Sql.Close();
        }
    }
    
    
