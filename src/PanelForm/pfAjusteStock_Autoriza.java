    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
        
        String Query="";

        btGuardar.setEnabled(false);
        
        
        //VALIDACIONES
        if(cbMotivo.getSelectedIndex()==-1 ||
                
            txComentarios.getText().isEmpty()||
            Grilla.getRowCount()==0){
           
            fmMain.Mensaje("Faltan Motivo o Comentario no puede ser vacio.");
            btGuardar.setEnabled(true);
            return;
        }
        
        
        
        if(fmMain.OkCancel("¿Desea realizar el ajuste?") != JOptionPane.OK_OPTION){
            btGuardar.setEnabled(true);
            return;
        }
        
        ExeSql Sql = new  ExeSql();
        ExeSql Sql2 = new  ExeSql();
        ResultSet Rs;
       
        int Pendiente = 0;
        
        try {
            
            
            for(int f=0;f< Grilla.getRowCount();f++){
            
                String Sku = Grilla.getValueAt(f, 0).toString().trim();
                
                int pend = AjustePendiente(Sku.trim()); 
            
                if (pend == 1){
              
                    Pendiente = 1;
             
                }
            
            }
            
            if (Pendiente == 1){
            
                fmMain.Mensaje("Hay productos con ajustes pendientes..! ");
                txSku.requestFocus();
                txSku.setText("");
                return;
            
            }
            
            
            Rs = Sql.Select("select sp_getcorrelativo \n" +
                            "from sp_getcorrelativo('AJU')");   
            Rs.next();
            
            String Folio = Rs.getString("sp_getcorrelativo");
            
            //Cabezera
            Sql.ExeSql( "INSERT INTO ajusteenc_autoriza\n" +
                        "(folio,motivo,comentario\n" +
                        ") \n" +
                        "VALUES (\n" +
                        Folio + ",\n" +
                        String.valueOf(cbId_Accion(cbMotivo)) + ",\n'" +
                        txComentarios.getText() +"'\n" +
                        ")");
            
           
            
            //Productos
            for(int i=0;i< Grilla.getRowCount();i++){
                
                
                Query = "INSERT INTO ajustedet_autoriza\n" +
                            "(folio,sku,cantidad,tipo,ubicacion,folioaud) \n" +
                            "VALUES (\n" +
                            Folio + ",\n'" +
                            Grilla.getValueAt(i, 0).toString().trim() + "',\n" +
                            fmMain.SetGuardar(Grilla.getValueAt(i, 4).toString().trim()) + ",\n" +
                            Grilla.getValueAt(i, 5).toString() + ",\n" +
                            "'" + Grilla.getValueAt(i, 7).toString().trim()+"',"+Grilla.getValueAt(i, 8).toString().trim() + ")\n";
                Sql2.ExeSql(Query); 
               
                                   
            } // Fin For de Detalle
             
            Sql.Commit();
            Sql2.Commit();
            
            fmMain.Mensaje("Guardado Ajuste N° "+Folio+" , quedará Pendiente de Autorización.  \n "+
                          "Los Movimientos se realizaràn cuando esté Autorizado el Ajuste. \n ");
            
            //AbrirFolio(Folio);
            
            Nuevo = 0;
            SetTipo(-1);
           
            
        } catch (Exception e) {
        
            System.out.println(e);
            Sql.Rollback();
            Sql2.Rollback();
            Logger.getLogger(pfAjusteStock.class.getName()).log(Level.SEVERE, null, e);
        
        } finally{
            Sql.Close();
            Sql2.Close();
        }
    }//GEN-LAST:event_btGuardarActionPerformed

