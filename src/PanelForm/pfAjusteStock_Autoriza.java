    private void CargaProducto(String Codigo){
        
        ResultSet Rs, Rs1;
        ExeSql Sql = new ExeSql();
        String Query ="";
        try {

            Rs = Sql.Select("select p.sku,p.nombre,u.unidad,i.stock \n" +
                            "from producto p\n" +
                            "left join inventario i on p.sku=i.sku\n" +
                            "left join par_unidad u on p.unidad=u.codigo \n" +
                            "where p.sku='"+ Codigo.trim() +"'");
            
            
            if (Rs.next()){
                
                txSku.setText(Rs.getString("Sku"));
                txNombre.setText(Rs.getString("Nombre").trim());
                txUnidad.setText(Rs.getString("Unidad").trim());
                txStockActual.setText(fmMain.FormatoNumero(Rs.getDouble("stock")));
                cbTipo.requestFocus();
                
                
                
                  if (Auditoria == 1){
                
                    BuscarAud(txSku.getText().trim());
                    
                  }
            
            }else{
            
                Query = "select p.codbar, p.sku,pr.nombre, i.stock, u.unidad \n" +
                                "from codbar p \n"+
                                "left join producto pr on p.sku = pr.sku \n" +
                                "left join inventario i on p.sku = i.sku \n" +
                                "left join par_unidad u on pr.unidad=u.codigo \n" +
                                "where (p.sku='" + Codigo.trim() + "' or p.codbar ='" + Codigo.trim() + "') and p.sku = pr.sku ";
                      
                      Rs1 = Sql.Select(Query );
                      
                      if ( Rs1.next()){
                            txSku.setText(Rs1.getString("Sku"));
                            txNombre.setText(Rs1.getString("Nombre").trim());
                            txUnidad.setText(Rs1.getString("Unidad").trim());
                            txStockActual.setText(fmMain.FormatoNumero(Rs1.getDouble("stock")));
                            cbTipo.requestFocus();
                            
                            if (Auditoria == 1){
                                
                               BuscarAud(txSku.getText().trim());
                                
                            }
                            
                      
                      }else{
                                JOptionPane.showMessageDialog(null,"Producto no encontrado");
                                return;
                      }
            }
            

        } catch (Exception e) {
            
        } finally{
            Sql.Close();
        }
            
        
        
    }
    
    
