    private void CuentasxCobrar(){
        
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        
        tCxc = 0;
        
        try{
            

           Rs = Sql.Select("SELECT rut, monto, pagado, (monto - pagado) as saldo, femision FROM cli_cuentasxcobrar \n" +
                           "WHERE monto > pagado and tipdocto IN ('BOC','FAC')  \n"+
                           "AND ( EXTRACT(YEAR FROM femision) = "+cbAgno.getSelectedItem()+" AND EXTRACT(MONTH FROM femision) = "+cbMes.getSelectedIndex()+")");
            
            if (Sql.GetRowCount() > 0){
            
                while (Rs.next()){
                   
                    tCxc = tCxc + Rs.getDouble("saldo");
                   
                }
            
            }
            
           
            txCxc.setText("$ "+formateador.format(Math.round(tCxc)));
          
            
        }catch (SQLException ex) {
        
            Logger.getLogger(pfBalanceGeneral.class.getName()).log(Level.SEVERE, null, ex);
        
        }
        
        
        
        
        
    }
    
    
    
    
