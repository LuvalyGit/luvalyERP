    private void CuentasxPagar(){
        
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        
        tCxp = 0;
        
        try{
            

           Rs = Sql.Select("SELECT rut, monto, pagado, (monto - pagado) as saldo, fpago FROM prv_cuentasxpagar \n" +
                           "WHERE monto > pagado and tipdocto IN ('BGA','BOG','CLI','COM','FAG','FAP')  \n"+
                           "AND ( EXTRACT(YEAR FROM fpago) = "+cbAgno.getSelectedItem()+" AND EXTRACT(MONTH FROM fpago) = "+cbMes.getSelectedIndex()+")");
            
            if (Sql.GetRowCount() > 0){
            
                while (Rs.next()){
                   
                    tCxp = tCxp + Rs.getDouble("saldo");
                   
                }
            
            }
            
           
            txCxp.setText("$ "+formateador.format(Math.round(tCxp)));
          
            
        }catch (SQLException ex) {
        
            Logger.getLogger(pfBalanceGeneral.class.getName()).log(Level.SEVERE, null, ex);
        
        }
        
        
        
        
        
    }
        
    
