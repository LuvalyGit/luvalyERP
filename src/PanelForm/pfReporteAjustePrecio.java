    public void abrir(String file ){
        
       try {
           String url = file;
           ProcessBuilder p = new ProcessBuilder();
           p.command("cmd.exe", "/c",url);
           p.start();
       } catch (IOException ex) {
           Logger.getLogger(pfReporteAjustePrecio.class.getName()).log(Level.SEVERE, null, ex);
       }
        
    }
    
    
