    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        if(TipoDocto.equals("FAT"))
        {
            if(Double.valueOf(txNumero.getText()) > Numero)
            {
                Msje("Validacion", "La cantidad no puede ser mayor al numero Original que es: " + Numero, x, y);
                txNumero.requestFocus();
            }
        }
        
        
        if (txNumero.getText().equals("")){
            
            txNumero.setText("0");
            
        }
        
        if (txPreciounit.getText().equals("")){
                Msje("Validacion", "Debe ingresar un valor para Precio Unitario", x, y);
                txPreciounit.requestFocus();
        }

        
        Numero = Double.valueOf(txNumero.getText());
        Preciounit = Double.valueOf(fmMain.SetGuardar(txPreciounit.getText()));
        PrecioDesc = Double.valueOf(fmMain.SetGuardar(lbDescuentoUnitario.getText()));
        
        dispose();
    }//GEN-LAST:event_btAceptarActionPerformed

