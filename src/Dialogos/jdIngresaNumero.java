    private void txTotalDescuentoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txTotalDescuentoKeyPressed

        if(evt.getKeyCode()== KeyEvent.VK_ENTER) {

            if (txTotalDescuento.getText().trim().isEmpty()){

                txTotalDescuento.setText("0");

            }

            int cant = (int) Math.round(Double.parseDouble(txNumero.getText().trim()));

            double descuento = Double.parseDouble(txTotalDescuento.getText().trim());

            double unitario = Double.parseDouble(txPreciounit.getText().trim());

            System.out.println("cant ES :"+cant);

            System.out.println("descuento ES :"+descuento);

            if (descuento > 0){

                unitario = unitario - (descuento/cant);
                
                
                 System.out.println("unitario ES :"+unitario);
                
                if (unitario < 0){
                
                
                    fmMain.Mensaje("Valor no puede ser negativo!!");
                    return;
                
                }
                
                

                txPreciounit.setText(""+fmMain.FormatoNumero2(descuento/cant));
                
                lbDescuentoUnitario.setText(""+fmMain.FormatoNumero2(unitario));

            }

        }

    }//GEN-LAST:event_txTotalDescuentoKeyPressed

