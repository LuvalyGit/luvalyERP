    public void CargaDatos(String Dia,int Mes, String Agno){
       
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel TableModel = (DefaultTableModel) Grilla.getModel();
        
        String Query = "";
        
        int agno = Integer.parseInt(Agno);
        int mes = (Mes+1);
        int dia = Integer.parseInt(Dia);
     
        
        while(TableModel.getRowCount()>0) 
              TableModel.removeRow(0);     
        
        try {
            
            Query = "select ce.numero as cheque,ce.rut, p.nombre, ce.monto, ce.pagado from cheques_emitidos ce\n" +
                    "left join proveedor p on ce.rut = p.rut\n" +
                    "where extract(year from ce.fvencimiento)= "+agno+" \n" +
                    "and extract(month from ce.fvencimiento)= "+mes+" \n" +
                    "and extract(day from ce.fvencimiento)= "+dia;
            
            
            
            Rs = Sql.Select(Query);
            
            
            while (Rs.next()){
                
                Double Saldo = (Rs.getDouble("monto")+Rs.getDouble("pagado"));
                
                
                TableModel.addRow(new Object[]{Rs.getString("cheque").trim(),
                                               Rs.getString("rut").trim(), 
                                               Rs.getString("nombre").trim(), 
                                               "$ "+fmMain.FormatoTotal(Rs.getDouble("monto")), 
                                               "$ "+fmMain.FormatoTotal(Rs.getDouble("pagado")), 
                                               "$ "+fmMain.FormatoTotal(Saldo) 
                });
                
                                
            }
            
            
             
        } catch (SQLException e) {
            
            JOptionPane.showMessageDialog(null, e);  
            Logger.getLogger(jdBuscaCheques.class.getName()).log(Level.SEVERE, null, e);
        
        } finally{
            Sql.Close();
        }
        
    }
   

    
