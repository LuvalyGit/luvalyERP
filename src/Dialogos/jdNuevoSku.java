    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        
        
        if(txNombreGenerico.getText().trim().isEmpty()){
        
            fmMain.Mensaje("Debe Ingresar un Nombre!!");
            return;
        
        }
        
        if(txMarca.getText().trim().isEmpty()){
        
            fmMain.Mensaje("Debe Ingresar una Marca!!");
            return;
        
        }
        
        if(txModelo.getText().trim().isEmpty()){
        
            fmMain.Mensaje("Debe Ingresar un Modelo!!");
            return;
        
        }
        
        
        if(txTallaCon.getText().trim().isEmpty()){
        
            fmMain.Mensaje("Debe Ingresar una Unidad de Medida!!");
            return;
        
        }
        
        
        
        
        if(txEmbalaje.getText().trim().isEmpty()){
        
            fmMain.Mensaje("Valor Embalaje no puede ser 0 !!");
            return;
        
        }        
        
        
        int embalaje  = Integer.parseInt(txEmbalaje.getText().trim());
        
        
        
        if(embalaje <=1){
        
            fmMain.Mensaje("Valor Embalaje no puede ser menor a 2 !!");
            return;
        
        }
        
        
        
        
        
        NombreCompleto = txNombreGenerico.getText().trim() + " "+ txMarca.getText().trim() +" "+txModelo.getText().trim()+" "+txTallaCon.getText().trim();
        
        
        System.out.println("La Longuitud ES  : "+NombreCompleto.trim().length());
        if (NombreCompleto.trim().length() > 80){
        
            fmMain.Mensaje("La longuitud del Nombre Completo ("+NombreCompleto.trim().length()+") es mayor a la permitida (80) !!");
            
            return;
        
        }
        
        
        
        dispose();
    }//GEN-LAST:event_btAceptarActionPerformed

