    private void Inicializa(){
       
        DefaultTableModel model = (DefaultTableModel) Grilla_Dias.getModel();
        
        while(model.getRowCount()>0){
            model.removeRow(0);
        }
        
        
        ExeSql Sql = new ExeSql();
        ResultSet Rs = null;
        
        try {
            
            Rs = Sql.Select("SELECT CAST(a.fecha as date) FROM agregados_mp a\n" +
                             "WHERE a.convenio = "+Convenio+" \n" +
                             "AND EXTRACT (YEAR FROM a.fecha)= " + cboAgno.getSelectedItem().toString() + "\n"+
                             "AND EXTRACT (MONTH FROM a.fecha)= " + cboMes.getSelectedItem().toString() + "\n"+
                             "GROUP BY CAST(a.fecha as date) \n"+
                             "ORDER BY CAST(a.fecha as date)");
            
            
            if (Sql.GetRowCount() > 0){
             
                while (Rs.next()){
                
                    model.addRow(new Object[]{dateFormat.format(Rs.getDate("fecha")), Convenio});
                
                }
             
             
            }

        
        } catch (SQLException ex) {
            Logger.getLogger(jdAgregados_MP.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
    }
    
    
    
