    public void CargaAgregados() {
        DefaultTableModel model = (DefaultTableModel) Grilla.getModel();
        
        
        while(model.getRowCount()>0){
            model.removeRow(0);
        }
       
        ExeSql Sql = new ExeSql();
        ResultSet Rs = null;
        
       
        try {
            
            
            String Fecha = Grilla_Dias.getValueAt(Grilla_Dias.getSelectedRow(),0).toString();
            
            Rs = Sql.Select("SELECT a.* FROM agregados_mp a\n" +
                            "WHERE a.convenio = "+Convenio+" \n"+
                            "AND CAST(a.fecha as date) = '"+Fecha+"'");
            
              
            if (Sql.GetRowCount() > 0){
              
                while (Rs.next()) {
                            
                    model.addRow(new Object[]{Rs.getInt("id"), 
                                              Rs.getString("producto"), 
                                              dateFormat.format(Rs.getDate("fecha")), 
                                              Rs.getInt("id_madre"),
                                              Rs.getInt("convenio")  
                                             
                                                  
                    });
                                                    
                }   
                            
              
            }
            
          
            
        } catch (SQLException ex) {
            Logger.getLogger(jdAgregados_MP.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally {
            Sql.Close();
        }
            
    }
    
    
    
    
    
   
