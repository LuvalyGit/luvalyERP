    private void btCerrarPeriodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCerrarPeriodoActionPerformed
        ExeSql  Sql = new ExeSql();
        if(fmMain.OkCancel("¿Desea cerrar las remuneraciones de "+cbMesCierre.getSelectedItem()+" de "+cbAgnoCierre.getSelectedItem()+"?")== JOptionPane.OK_OPTION){
        try
                {
            
            Sql.ExeSql("insert into psueldosconta \n" +
                                    "(agno,mes) values (" + 
                                    cbAgnoCierre.getSelectedItem().toString().trim()+ "," +
                                    (cbMesCierre.getSelectedIndex()+1) + ")");
            Sql.Commit();
            JOptionPane.showMessageDialog(null, "Periodo cerrado con exito");
            
        }
        catch (Exception e) {
                    Sql.Rollback();
                    JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
                } finally{
                    Sql.Close();
                    RevisaMes();
                }
            
        }
    }//GEN-LAST:event_btCerrarPeriodoActionPerformed
    public void Botones()
       {
           if(cerrado==false)
           {
               btCerrarPeriodo.setEnabled(true);
               btReabrir.setEnabled(false);
           }
           else
           {
               btCerrarPeriodo.setEnabled(false);
               btReabrir.setEnabled(true);
           }  
       }
    
    public void RevisaMes() {
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        try {

            Rs = Sql.Select("select count (agno) as cuantos from psueldosconta where  \n"
                    + "agno=" + cbAgnoCierre.getSelectedItem().toString().trim() + " and mes=" + (cbMesCierre.getSelectedIndex() + 1));

            Rs.next();

            if (Rs.getInt("cuantos") >= 1) {
                cerrado = true;
            } else {
                cerrado = false;
            }
            Botones();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        } finally {
            Sql.Close();
        }
    }
    
    
