    public boolean CheckIfAutorizable () {
        boolean autorizable = false;
        String rut = Grillas_pendientes.getValueAt(Grillas_pendientes.getSelectedRow(), 0).toString();
        String nro_old = Grillas_pendientes.getValueAt(Grillas_pendientes.getSelectedRow(), 1).toString();
        String nro_new = Grillas_pendientes.getValueAt(Grillas_pendientes.getSelectedRow(), 2).toString();
        String fecha_old = Grillas_pendientes.getValueAt(Grillas_pendientes.getSelectedRow(), 3).toString();
        String fecha_new = Grillas_pendientes.getValueAt(Grillas_pendientes.getSelectedRow(), 4).toString();
        String usuario_ = Grillas_pendientes.getValueAt(Grillas_pendientes.getSelectedRow(), 5).toString();
        String tipo = Grillas_pendientes.getValueAt(Grillas_pendientes.getSelectedRow(), 6).toString();
        String autorizado = Grillas_pendientes.getValueAt(Grillas_pendientes.getSelectedRow(), 7).toString();
        ExeSql Sql = new ExeSql();
        ResultSet Rs = null;
        String Query = "select autorizado from ctacteprvmod\n"
                + "where rut_proveedor = "+rut+" and nro_old = "+nro_old+" and nro_new = "+nro_new+" and fecha_old = '"+fecha_old+"' and fecha_new = '"+fecha_new+"' \n"
                + "and tipdocto = '"+tipo+"';";
        
        try {
            Rs = Sql.Select(Query);
            while(Rs.next()){
                if(Rs.getBoolean("autorizado")){
                    autorizable = false;
                    break;
                }
                else {
                    autorizable=true;
                    break;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(jdAutorizaModificacion.class.getName()).log(Level.SEVERE, null, ex);
        }
        return autorizable;
    }

