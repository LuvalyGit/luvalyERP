    public void LlenarGrillaPendientes (String sql_extra) {//Se llena grilla pendientes y se recibe texto que se agrega a consulta
            DefaultTableModel model = (DefaultTableModel) Grillas_pendientes.getModel();
            Grillas_pendientes.setDefaultEditor(Object.class, null);
            pfModificarIngreso.LimpiaGrilla(model);
            String Query = "select rut_proveedor, nro_old, nro_new, fecha_old, fecha_new, usuario, tipdocto, autorizado "
                    + "from ctacteprvmod \n";
                    Query = Query + sql_extra;
                    
        try {
            ExeSql Sql = new ExeSql();
            ResultSet Rs;
            Rs = Sql.Select(Query);

            for(int i = 0;Rs.next(); i++){
                model.addRow(new Object[]{Rs.getInt("rut_proveedor"),Rs.getString("nro_old"),Rs.getString("nro_new"), Rs.getString("fecha_old"),Rs.getString("fecha_new"),Rs.getString("usuario"),Rs.getString("tipdocto"),Rs.getBoolean("autorizado")});
            }
            Grillas_pendientes.changeSelection(0, 0 , false, false);

            
        }
        catch (SQLException ex) {
            //Logger.getLogger(pfModificarIngreso.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
            
