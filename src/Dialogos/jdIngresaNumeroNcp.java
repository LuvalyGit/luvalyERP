    public void Msje (String Titulo, String Mensaje, int xx , int yy){
            JOptionPane pane = new JOptionPane(Mensaje);  
            JDialog dialog = pane.createDialog(Titulo);  
            dialog.setLocation(xx, yy);  
            dialog.setVisible(true);  
    }
    
    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
      
        
        if (txCantNueva.getText().trim().equals("") || txCantNueva.getText().trim().equals("0") ){
            
            txCantNueva.setText("0");
            Msje("Validacion", "La cantidad no puede ser 0 !! " , x, y);
            txCantNueva.requestFocus();
            return;
            
            
        }
        
        
        if(Double.valueOf(txCantNueva.getText()) > CantActual){
                
            Msje("Validacion", "La cantidad no puede ser mayor a lo existente en Nota de Cr√©dito Proveedor !!" , x, y);
            txCantNueva.requestFocus();
            return;
        }
        
                
        CantNueva = Double.valueOf(txCantNueva.getText());
        
        
        dispose();
    }//GEN-LAST:event_btAceptarActionPerformed

