    private void carga_producto(){
         String codbarfinal = txSku.getText().replace("'", "-");
        txSku.setText(codbarfinal.trim());
        if(!txSku.getText().isEmpty())
             CargaProducto(txSku.getText());
        else if(!txSku.getText().isEmpty())
            txNombre.requestFocus();
        else{
            jdBuscarProductos BP = new jdBuscarProductos(null, true);
            BP.setLocationRelativeTo(null);
            BP.setTitle("Buscar Producto");
            BP.setVisible(true);
            if(!"".equals(BP.GetCodigo()))
                CargaProducto(BP.GetCodigo());
        }
    }
    
    private void CargaProducto(String Codigo){
        ExeSql  Sql = new ExeSql();
        ResultSet Rs, Rs2;
        try{
            Rs = Sql.Select("select p.discontinuado, p.sku,p.nombre,u.unidad,u.um \n" +
                            "from producto p\n" +
                            "left join par_unidad u\n" +
                            "on u.codigo=p.unidad\n" +
                            "where p.sku='" + Codigo + "'");
            Rs.next();
            txSku.setText(Rs.getString("sku"));
            txNombre.setText(Rs.getString("nombre"));
            txUnidad.setText(Rs.getString("unidad"));
            txUM.setText(Rs.getString("um"));
            

            
            if (Rs.getBoolean("discontinuado")==true)
            {
                fmMain.disc=1;
            }
            else
            {
               fmMain.disc=0;
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            }finally{
            Sql.Close();
        }
        
    }
    
    
    
    public void buscar_sku_asociado(String codbar){
        
        ExeSql  Sql = new ExeSql();
        ResultSet Rs;
        try{
            Rs = Sql.Select("select p.sku,p.nombre,p.discontinuado,u.unidad,u.um \n" +
                            "from codbar cb\n" +
                            "left join producto p on cb.sku = p.sku \n" +
                            "left join par_unidad u on u.codigo=p.unidad\n" +
                            "where cb.codbar IN ('" + codbar + "') and cb.rutprv  = "+txRutChilemat.getText().trim());
            
            
            if (Sql.GetRowCount() > 0){
                        
                Rs.next();
                
                
                Asociado = 1;
                
                txSku.setText(Rs.getString("sku"));
                txNombre.setText(Rs.getString("nombre"));
                txUnidad.setText(Rs.getString("unidad"));
                txUM.setText(Rs.getString("um"));
            
                if (Rs.getBoolean("discontinuado")==true){
                
                    fmMain.disc=1;
                
                }else{
               
                    fmMain.disc=0;
                }
                
                           
            }else{
            
            
                Asociado = 0;
            
            }
            
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            }finally{
            Sql.Close();
        }
        
    
    }
    
    
    
