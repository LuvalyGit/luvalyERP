    private void btGuardaIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardaIDActionPerformed
        
 //       Verifica_ID(convenio,txIDChilecompras.getText().toString().trim());
        
        
//        if (!existe){
//           
//           fmMain.Mensaje("Codigo No Existe en el Reporte!");
//           return;
//            
//        
//        }
        
        
        
        
        if (lbSku.getText().toString().trim().equals("")){
            
            fmMain.Mensaje("Imposible guardar, revise que el codigo sea valido: ");
            return;
        }
        
        if (txIDChilecompras.getText().toString().trim().equals("") || txCantidad.getText().toString().trim().equals("") ){
            
            fmMain.Mensaje("Debe Ingresar el ID o la Cantidad: ");
            return;
        }
        
        if (txCantidad.getText().toString().trim().equals("0") ){
            
            fmMain.Mensaje("La Cantidad mínima debe ser 1");
            return;
        }
        
        
        
        ResultSet Rs;
        ExeSql Sql = new ExeSql();
        ExeSql Sql2 = new ExeSql();
        
        try{
              
            
             Rs = Sql2.Select("select * from codchile where idch ='"+txIDChilecompras.getText().toString().trim()+"'");
            
             if (Sql2.GetRowCount() == 0){
            
                if (lbIdMadre.getText().trim().isEmpty()){
                
                    lbIdMadre.setText("0");
                
                } 
                 
                 
                Sql.ExeSql("INSERT into codchile (sku,idch,cant_unidad,usuario,idch_madre,convenio) VALUES ("
                          + "'" + lbSku.getText().toString().trim() + "',"
                          + "'" + txIDChilecompras.getText().toString().trim() + "',"
                          + txCantidad.getText().toString().trim() + ","
                          + "'" + fmMain.GetUsuario() + "','"
                          + lbIdMadre.getText().trim()+ "',"
                          + convenio+ ")");
                    
                Sql.Commit();
        
        
            }else if (Sql2.GetRowCount() > 0) {
             
              Rs.next();
                
                
              if(!lbSku.getText().trim().equals(Rs.getString("sku").trim())){
              
                   fmMain.Mensaje("Codigo ya está asociado a Sku N° "+Rs.getString("sku").trim());
                   
                   txIDChilecompras.setText("");
                   txCantidad.setText("");
                   txIDChilecompras.requestFocus();
                   
                   return;
              }  
                
                
              Sql.ExeSql( "UPDATE codchile SET \n"+
                          "sku = '" + lbSku.getText().toString().trim() +"', \n"+
                          "cant_unidad = " + txCantidad.getText().toString().trim() +", \n"+
                          "idch_madre = '" + lbIdMadre.getText().trim() +"' \n"+
                          "WHERE idch = '" + txIDChilecompras.getText().toString().trim() + "'");
                
             
               Sql.Commit();
             
             
             
             }
             
             
             
             Asociar_Al_Reporte(convenio,lbSku.getText().toString().trim(),txIDChilecompras.getText().toString().trim(),lbIdMadre.getText().trim());
             
             
        
             fmMain.Mensaje("ID "+txIDChilecompras.getText().toString().trim()+" asociada correctamente a: "+lbSku.getText().toString().trim());
             
              dispose();
        
        }
        catch (Exception e){  
            Sql.Rollback();
            fmMain.Mensaje("Error al guardar: "+e);
        }
        finally{
            Sql.Close();
           
        }
    }//GEN-LAST:event_btGuardaIDActionPerformed

