    private void btGuardaIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardaIDActionPerformed
        
        
        if (lbSku.getText().toString().trim().isEmpty()){
            
            fmMain.Mensaje("Imposible guardar, revise que el codigo sea valido: ");
            return;
        }
        
        if (txIDChilecompras.getText().toString().trim().isEmpty()  ){
            
            fmMain.Mensaje("Debe Ingresar el ID!! ");
            return;
        }
        
        
        Verifica_SKU(convenio,lbSku.getText().toString().trim());
        
        
        if (!existe){
           
           fmMain.Mensaje("Codigo No Existe en el Reporte!");
           return;
            
        
        }
        
        
        ResultSet Rs;
        ExeSql Sql = new ExeSql();
        ExeSql Sql2 = new ExeSql();
        
        try{
              
            
            Rs = Sql.Select("select * from codchile where sku IN ('"+lbSku.getText().toString().trim()+"')");
            
            if (Sql.GetRowCount() > 0) {
             
                while(Rs.next()){   
                
                    Sql2.ExeSql( "UPDATE codchile SET \n"+
                                 "idch_madre = '" + txIDChilecompras.getText().toString().trim() +"' \n"+
                                 "WHERE sku IN ('" + lbSku.getText().toString().trim() + "')");
             
                    
             
                }
             
                Sql2.Commit();
            }
             
            Asociar_Al_Reporte(convenio,lbSku.getText().toString().trim(),txIDChilecompras.getText().toString().trim());
            
            fmMain.Mensaje("ID MADRE "+txIDChilecompras.getText().toString().trim()+" asociada correctamente a: "+lbSku.getText().toString().trim());
        
        }
        catch (SQLException ex){  
            Sql.Rollback();
            Logger.getLogger(jdAgregaIDCHMADRE.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            Sql.Close();
            dispose();
        }
    }//GEN-LAST:event_btGuardaIDActionPerformed
    
