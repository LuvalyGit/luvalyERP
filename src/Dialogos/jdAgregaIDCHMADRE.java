    private void Verifica_SKU(int convenio,String sku){
            
        ExeSql Sql = new ExeSql();
       
        ResultSet Rs;
        
        
        try {
            
            String tabla = "";
                       
            
            if (convenio == 2){
                
                tabla = "ferreteria";
                
                
            }else if (convenio == 3) {
                
                tabla = "aseo";
                
                
            }else if (convenio == 4) {
                
                tabla = "alimentos";
                
                
            }else if (convenio == 11) {
                
                tabla = "emergencia";
                
                
            }else if (convenio == 7) {
                
                tabla = "escritorio";
                
                
            }
            
            
            
            Rs = Sql.Select("SELECT * FROM "+tabla+" WHERE sku IN('"+sku.trim()+"')");
            
            if (Sql.GetRowCount() > 0){
                
                existe = true;
                
                
            }else {
            
                existe = false;
            
            }
        
        
        
        
        } catch (SQLException ex) {
            
            Logger.getLogger(pfProductos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         
    } 
    
    
    
    private void Asociar_Al_Reporte(int convenio,String sku,String idch){
            
        ExeSql Sql = new ExeSql();
        ExeSql Sql2 = new ExeSql();
        ResultSet Rs;
        
        int idmadre = Integer.parseInt(idch.trim());
        
        try {
            
            String tabla = "";
            
            if (convenio == 2){
                
                tabla = "ferreteria";
                
                
            }else if (convenio == 3) {
                
                tabla = "aseo";
                
                
            }else if (convenio == 4) {
                
                tabla = "alimentos";
                
                
            }else if (convenio == 11) {
                
                tabla = "emergencia";
                
                
            }else if (convenio == 7) {
                
                tabla = "escritorio";
                
                
            }
            
            
            
            Rs = Sql.Select("SELECT * FROM "+tabla+" WHERE sku IN ('"+sku.trim()+"')");
            
            if (Sql.GetRowCount() > 0){
                
                while (Rs.next()){
                
                    Sql2.ExeSql("UPDATE "+tabla+" SET \n"+
                                "id_madre = " + idmadre +" \n"+
                                "WHERE sku IN('" + sku.trim()+"')");
                
                }
                
                Sql2.Commit();
             
                
            }
        
        
        } catch (SQLException ex) {
            Sql2.Rollback();
            Logger.getLogger(jdAgregaIDCHMADRE.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         
    }
    
    
     
     
     
    
