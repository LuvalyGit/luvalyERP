    public void Carga() {
        ExeSql sql = new ExeSql();
        ResultSet rs = null;
        String query = "select f.tipdocto, f.monto, f.folio, f.nrodocto, (select nombre from par_general where tipo = 'FLETE' and codigo = f.transporte) transporte, f.asociada from flete f where f.tipdocto = 'GDP' and f.nrodocto in ("+getNrodocto()+") ";
        try {
            rs = sql.Select(query);
            DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
            while(table.getRowCount()>0) table.removeRow(0);
            for(int i = 0; rs.next(); i++){
                table.addRow(new Object[] {
                    rs.getString("tipdocto"),
                    rs.getInt("nrodocto"),
                    rs.getInt("folio"),
                    rs.getString("transporte"),
                    rs.getInt("monto"),
                    rs.getBoolean("asociada")
                });
            }
            jTable1.setModel(table);
        } catch (SQLException ex) {
            Logger.getLogger(jdFlete.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
